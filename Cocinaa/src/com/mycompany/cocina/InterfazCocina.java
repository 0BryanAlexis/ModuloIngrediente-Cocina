    public void mostrar(){
        int idComanda = 0, idPlatillo = 0, cantidadPlatillo = 0;
        String sql = "select idComanda, idPlatillo, cantidad FROM platillosporcomanda";
        Statement st;
        ConexionBD conn = new ConexionBD();
        Connection ConexionBD = conn.getConnection();
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("idComanda");
        model.addColumn("idPlatillo");
        model.addColumn("Cantidad");
        TablaEntrada.setModel(model);
        
        try{
            st=ConexionBD.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while(rs.next()){
                datos[0]=rs.getString(1);
                datos[1]=rs.getString(2);
                datos[2]=rs.getString(3);
                model.addRow(datos);
        }
            } catch(Exception e){
                JOptionPane.showMessageDialog(null,"Error"+ e.toString());
            }
        
        }
